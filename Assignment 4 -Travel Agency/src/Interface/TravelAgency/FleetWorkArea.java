    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)FleetTable.getModel();
        dtm.setRowCount(0);
        
        for(Fleet a : fleetDirectory.getFleetDirectory()){
            if(airliner.getairlinerName().equalsIgnoreCase(a.getairPlaneName()))
            {
            Object[] row = new Object[dtm.getColumnCount()];
            
            row[0]=a;
            row[1]=a.getSeats();
            dtm.addRow(row);
            }
    }
    }
