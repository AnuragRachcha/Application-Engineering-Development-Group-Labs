    private void btnConfirmBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmBookingActionPerformed
        // TODO add your handling code here:

        // int value=fd.getSeats();
        if(txtName.getText().isEmpty()|| txtPhone.getText().isEmpty()|| txtPassportNumber.getText().isEmpty()){
             JOptionPane.showMessageDialog(null,"Please Enter all the fields");
        }
        else if(!phonePatternChecker()){
            JOptionPane.showMessageDialog(null, "Please Enter Mobile Number Properly");
        }
        else{
        int selectedrow=custflighttbl.getSelectedRow();
        Flightdetails fd=(Flightdetails)custflighttbl.getValueAt(selectedrow, 1);
        String name=txtName.getText().trim();
        String phoneNumber=txtPhone.getText().trim();
        String passport=txtPassportNumber.getText().trim();
        Customer c=customerDirectory.addCustomer();
        c.setName(name);
        c.setphoneNumber(phoneNumber);
        c.setpassportNumber(passport);
        c.setSeat(String.valueOf(comboSeatSelection.getSelectedItem()));
        c.setDate(fd.getDate());
        c.setflighNumber(fd.getflighNumber());
        c.setAirlineName(fd.getairPlaneName());
        c.setAirplaneName(fd.getModelNumber());
        
        for (Map.Entry<String,Boolean> entry : fd.getH().entrySet()){
         if(entry.getKey().equals(String.valueOf(comboSeatSelection.getSelectedItem())) ){
         entry.setValue(Boolean.FALSE);
         }
        }
        int value=0;
        value=fd.getSeats();
        if(value>0)
        {
        value--;
        fd.setSeats(value);
        }
        else{
        JOptionPane.showMessageDialog(null,"Seats not available");
        }
        c.setflighNumber(fd.getflighNumber());
        c.setSource(fd.getSource());
        c.setDestination(fd.getDestination());
        c.setPrice(fd.getPrice());
        //c.setDate(fd.getDate());
        
        if(selectedrow>=0 && !name.isEmpty())
        {
            
            JOptionPane.showMessageDialog(null,"Successfully Booked!!");
            populate10();
            
            txtName.setEditable(false);
        txtPhone.setEditable(false);
        txtPassportNumber.setEditable(false);
        
        txtName.setText("");
        txtPhone.setText("");
        txtPassportNumber.setText("");
        comboSeatSelection.removeAllItems();
        }
        else{
         JOptionPane.showMessageDialog(null,"Cannot Book. Select flight first.");
        }
        }
        
    }//GEN-LAST:event_btnConfirmBookingActionPerformed

    private void txtPhoneKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPhoneKeyPressed
        // TODO add your handling code here:
        int key=evt.getKeyCode();
        if((key>=evt.VK_0 && key<=evt.VK_9)|| key==KeyEvent.VK_BACKSPACE)
        {
        txtPhone.setEditable(true);
        }
        else{
        txtPhone.setEditable(false);
        }
            
    }//GEN-LAST:event_txtPhoneKeyPressed

