    public void populate10(){
    
        int rowCount = custflighttbl.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)custflighttbl.getModel();
        dtm.setRowCount(0);
        
     
        Collections.sort(customerSearch);
        for (Flightdetails fdd : customerSearch) {
            Object[] row = new Object[dtm.getColumnCount()];
     
        row[0]=fdd.getName();
        row[1]=fdd;
        row[2]=fdd.getSource();
        row[3]=fdd.getDestination();
        row[4]=fdd.getDate();
        row[5]=fdd.getPrice();
        row[6]=fdd.getSeats();
        dtm.addRow(row);
        }
    
    }

    
